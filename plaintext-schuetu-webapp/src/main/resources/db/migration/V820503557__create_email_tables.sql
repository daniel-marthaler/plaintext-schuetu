-- Create Email tables
CREATE TABLE IF NOT EXISTS EMAIL (
  ID BIGSERIAL,
  BCC_ADDRESS VARCHAR(255),
  BODY TEXT,
  CC_ADDRESS VARCHAR(255),
  CREATED_AT TIMESTAMP NOT NULL,
  DIRECTION VARCHAR(255) NOT NULL,
  ERROR_MESSAGE TEXT,
  FROM_ADDRESS VARCHAR(255) NOT NULL,
  HTML BOOLEAN NOT NULL,
  MANDAT VARCHAR(255) NOT NULL,
  MAX_RETRIES INTEGER,
  MESSAGE_ID VARCHAR(255),
  RECEIVED_AT TIMESTAMP,
  RETRY_COUNT INTEGER,
  SENT_AT TIMESTAMP,
  STATUS VARCHAR(255) NOT NULL,
  SUBJECT VARCHAR(255),
  TO_ADDRESS VARCHAR(255) NOT NULL,
  CONFIG_NAME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS EMAIL_ATTACHMENT (
  ID BIGSERIAL,
  CONTENT_TYPE VARCHAR(255),
  CREATED_AT TIMESTAMP NOT NULL,
  DATA BYTEA NOT NULL,
  FILENAME VARCHAR(500) NOT NULL,
  SIZE_BYTES BIGINT,
  EMAIL_ID BIGINT NOT NULL,
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS EMAIL_CONFIG (
  ID BIGSERIAL,
  CREATED_AT TIMESTAMP NOT NULL,
  IMAP_DELETE_AFTER_FETCH BOOLEAN,
  IMAP_ENABLED BOOLEAN,
  IMAP_FOLDER VARCHAR(255),
  IMAP_HOST VARCHAR(255),
  IMAP_MARK_AS_READ BOOLEAN,
  IMAP_PASSWORD VARCHAR(255),
  IMAP_PORT INTEGER,
  IMAP_USE_SSL BOOLEAN,
  IMAP_USERNAME VARCHAR(255),
  MANDAT VARCHAR(255) NOT NULL,
  SMTP_FROM_ADDRESS VARCHAR(255),
  SMTP_FROM_NAME VARCHAR(255),
  SMTP_HOST VARCHAR(255),
  SMTP_PASSWORD VARCHAR(255),
  SMTP_PORT INTEGER,
  SMTP_USE_SSL BOOLEAN,
  SMTP_USE_TLS BOOLEAN,
  SMTP_USERNAME VARCHAR(255),
  UPDATED_AT TIMESTAMP,
  SMTP_ENABLED BOOLEAN,
  IMAP_POLL_INTERVAL INTEGER,
  CONFIG_NAME VARCHAR(255),
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS EMAIL_CONFIG_V2 (
  ID BIGSERIAL,
  CONFIG_NAME VARCHAR(255) NOT NULL,
  CREATED_AT TIMESTAMP NOT NULL,
  IMAP_DELETE_AFTER_FETCH BOOLEAN,
  IMAP_ENABLED BOOLEAN,
  IMAP_FOLDER VARCHAR(255),
  IMAP_HOST VARCHAR(255),
  IMAP_MARK_AS_READ BOOLEAN,
  IMAP_PASSWORD VARCHAR(255),
  IMAP_POLL_INTERVAL INTEGER,
  IMAP_PORT INTEGER,
  IMAP_USE_SSL BOOLEAN,
  IMAP_USERNAME VARCHAR(255),
  MANDAT VARCHAR(255) NOT NULL,
  SMTP_ENABLED BOOLEAN,
  SMTP_FROM_ADDRESS VARCHAR(255),
  SMTP_FROM_NAME VARCHAR(255),
  SMTP_HOST VARCHAR(255),
  SMTP_PASSWORD VARCHAR(255),
  SMTP_PORT INTEGER,
  SMTP_USE_SSL BOOLEAN,
  SMTP_USE_TLS BOOLEAN,
  SMTP_USERNAME VARCHAR(255),
  UPDATED_AT TIMESTAMP,
  PRIMARY KEY (ID)
);
